program = bytearray([
    0x10, 0x1,  0x5, 0x0, 0x0, 0x0,  0x0, 0x0, 0x0, 0x0, # push uint 5
    0x10, 0x1,  0x7, 0x0, 0x0, 0x0,  0x0, 0x0, 0x0, 0x0, # push uint 7
    0x20, # add 
    0x12, # dupe
    0x1, # print
    0x14, 0x4, # cast str 
    0x1, # print
    0x10, 0x4,  0x4, 0x0, 0x0, 0x0,  0x0, 0x0, 0x0, 0x0,
        0xA, 0x41, 0x42, 0xA, # push str "\nAB\n"
    0x1, # print 
    0xFF, # halt
])

with open("program.bin", "wb") as f:
    f.write(program)
